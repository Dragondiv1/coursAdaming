package com.adaming.bo;

public class Employe {
	
	private int id;
	private String nom;
	
	public Employe() {
		super();
	}
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getNom() {
		return nom;
	}
	
	public void setNom(String nom) {
		this.nom = nom;
	}

	
}
